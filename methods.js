function clickHome() {
    var x = document.getElementById('Home1');
    x.style.display = 'block';
    x = document.getElementById('About1');
    x.style.display = 'none';
    x = document.getElementById('Portfolio1');
    x.style.display = 'none';
    x = document.getElementById('Contact1');
    x.style.display = 'none';
}
